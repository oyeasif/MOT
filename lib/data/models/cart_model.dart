class ProductModel {
  final String name;
  final String color;
  final String size;
  final int quantity;
  final String imagePath;
  final String deleteIconPath;
  final String date;
  final double price;

  ProductModel({
    required this.name,
    required this.color,
    required this.size,
    required this.quantity,
    required this.imagePath,
    required this.deleteIconPath,
    required this.date,
    required this.price,
  });
}
